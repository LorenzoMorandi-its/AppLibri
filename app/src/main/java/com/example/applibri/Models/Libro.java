package com.example.applibri.Models;

public class Libro {
    public String titolo;
    public String autore;
    public String descrizione;
    public int pagine;

    public Libro (String titolo, String autore, String descrizione, int pagine){
        this.titolo=titolo;
        this.autore=autore;
        this.descrizione=descrizione;
        this.pagine=pagine;


    }
}